<p>按ctrl一次选中四个直线添加动画直线，然后再高级动画中继续添加螺旋状动画，单击下拉，对四个螺旋转设置开始为与上一动画同时，期间选择1秒，在效果中选择180度顺时针，关闭幻灯片母版</p>
